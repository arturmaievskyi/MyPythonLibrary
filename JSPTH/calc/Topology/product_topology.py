    # ===== DIAGONAL & SPECIAL MAPS =====

    def diagonal_map(self):
        """
        Get diagonal map δ: X → X × X
        δ(x) = (x, x)
        
        Only makes sense for single space product.
        """
        if self.dimensions != 2 or self.spaces[0] != self.spaces[1]:
            raise ValueError("Diagonal map requires X × X")
        
        def diagonal(x):
            if x not in self.spaces[0].base_set:
                raise ValueError("Point not in space")
            return (x, x)
        
        return diagonal

    def diagonal_set(self):
        """Get diagonal set (image of diagonal map)."""
        if self.dimensions != 2:
            raise ValueError("Diagonal only defined for 2-dimensional products")
        
        diagonal_set = set()
        
        # Points (x, x) where x ∈ X
        for x in self.spaces[0].base_set:
            if self.spaces[1] == self.spaces[0]:
                diagonal_set.add((x, x))
        
        return diagonal_set

    def is_diagonal_closed(self):
        """Check if diagonal is closed in product."""
        diagonal = self.diagonal_set()
        return self.is_closed_set(diagonal)

    def is_diagonal_open(self):
        """Check if diagonal is open in product."""
        diagonal = self.diagonal_set()
        return self.is_open_set(diagonal)

    # ===== PRODUCT OPERATIONS =====

    def product_with(self, other):
        """
        Create product of this product with another space.
        
        (X₁ × X₂) × X₃ = X₁ × X₂ × X₃
        """
        if isinstance(other, ProductTopology):
            combined_spaces = list(self.spaces) + list(other.spaces)
        else:
            combined_spaces = list(self.spaces) + [other]
        
        return ProductTopology(combined_spaces,
                              name=f"{self.name} × {getattr(other, 'name', 'Space')}")

    def extract_component_topology(self, coordinate_index):
        """Get the topology on a specific component."""
        if coordinate_index >= self.dimensions:
            raise ValueError("Invalid coordinate index")
        
        return self.spaces[coordinate_index]

