    # ===== DIMENSION =====

    def dimension(self):
        """
        Compute dimension of product.
        
        dim(X × Y) = dim(X) + dim(Y) (in many cases)
        """
        if self.size == 0:
            return -1
        elif self.size == 1:
            return 0
        else:
            # Sum of component dimensions (simplified)
            total_dim = 0
            for space in self.spaces:
                if hasattr(space, 'dimension'):
                    total_dim += space.dimension()
                else:
                    total_dim += 1
            
            return total_dim

    def is_zero_dimensional(self):
        """Product is zero-dimensional iff all components are."""
        return all(space.is_zero_dimensional() if hasattr(space, 'is_zero_dimensional')
                  else True for space in self.spaces)

