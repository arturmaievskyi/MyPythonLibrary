    # ===== ADVANCED ANALYSIS =====

    def hausdorff_dimension(self):
        """Approximate Hausdorff dimension."""
        if self.size <= 1:
            return 0
        
        return len(set(range(len(self.open_sets)))) / len(self.base_set) if self.base_set else 0

    def covering_dimension(self):
        """Covering dimension."""
        return max(0, len(self.open_sets) - 2)

    def basis_dimension(self):
        """Dimension of basis."""
        # All basis elements with >1 point
        basis_elements = [s for s in self.open_sets if len(s) > 1]
        return len(basis_elements)

    def generate_basis(self):
        """Generate basis for topology."""
        basis = set()
        
        # Basis consists of products of component opens
        component_opens = [space.open_sets for space in self.spaces]
        
        for open_combo in itertools_product(*component_opens):
            product_open = set()
            for element in itertools_product(*open_combo):
                product_open.add(element)
            
            basis.add(frozenset(product_open))
        
        return basis

    def subbasis_dimension(self):
        """Dimension of subbasis (component open sets)."""
        return sum(len(space.open_sets) for space in self.spaces)

    def fibration_analysis(self, coordinate_index):
        """
        Analyze fibration structure.
        
        Fiber over point p: F_p = {x ∈ X₁ × X₂ : π_i(x) = p}
        """
        fiber_count = len(self.spaces[coordinate_index].base_set)
        total_fibers = len(self.spaces[coordinate_index].base_set)
        points_per_fiber = self.size // total_fibers if total_fibers > 0 else 0
        
        return {
            'coordinate': coordinate_index,
            'fiber_space': self.spaces[coordinate_index],
            'base_space_size': len(self.spaces[coordinate_index].base_set),
            'points_per_fiber': points_per_fiber,
            'total_fibers': total_fibers,
        }

    # ===== SPECIAL PRODUCTS =====

    def power_space(self, n):
        """Get n-th power: X^n = X × X × ... × X (n times)."""
        if n <= 0:
            raise ValueError("n must be positive")
        
        if n == 1 and self.dimensions == 1:
            return self
        
        # Repeat first component n times
        base_space = self.spaces[0] if self.dimensions == 1 else self
        repeated = [base_space] * n
        
        return ProductTopology(repeated, name=f"{base_space.name}^{n}")

